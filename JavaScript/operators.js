// Mathematical operators

const a = 10;
const b = 5;

console.log(a + b); // 15 (addition)
console.log(a - b); // 5 (subtraction)
console.log(a * b); // 50 (multiplication)
console.log(a / b); // 2 (division)

// Comparison operators
const x = 10;
const y = "10";

console.log(x == y); // true (value equality)
console.log(x === y); // false (value and type equality)
console.log(x != y); // false (value inequality)
console.log(x !== y); // true (value and type inequality)

// Logical operator
const isAdult = true;
const hasPermission = false;

console.log(isAdult && hasPermission); // false (AND)
console.log(isAdult || hasPermission); // true (OR)
console.log(!isAdult); // false (NOT)
